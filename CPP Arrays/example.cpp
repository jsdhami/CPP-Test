#include <iostream>
using namespace std;

int main() {
  // Declare an array of integers with size 5
  int arr[5];

  // Initialize the array with values
  arr[0] = 10;
  arr[1] = 20;
  arr[2] = 30;
  arr[3] = 40;
  arr[4] = 50;

  // Print the values of the array
  for (int i = 0; i < 5; i++) {
    cout << "arr[" << i << "] = " << arr[i] << endl;
  }




  

  return 0;
}
